
# Defining variables for different data types
integer_var = 42  # Integer
boolean_var = True  # Boolean
char_var = 'A'  # Single character (treated as a string of length 1 in Python)
float_var = 3.14  # Float
double_var = 3.14159265358979  # Double precision (uses Python's float type)

# Printing variables on the console
print("Integer Value:", integer_var)
print("Boolean Value:", boolean_var)
print("Character Value:", char_var)
print("Float Value:", float_var)
print("Double Value (approximated using Python's float):", double_var)