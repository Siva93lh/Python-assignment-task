#Increment and Decrement Operators

def increment_and_decrement():
    # Initial value
    num = 10
    print("Initial value:", num)
    
    # Increment equivalent (++)
    num += 1
    print("After increment (++):", num)
    
    # Decrement equivalent (--)
    num -= 1
    print("After decrement (--):", num)

# Call the method
increment_and_decrement()