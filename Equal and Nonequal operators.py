
# Define two numbers
num1 = 10
num2 = 20

# Using the equality operator (==)
if num1 == num2:
    print("The numbers are equal.")
else:
    print("The numbers are not equal.")

# Using the inequality operator (!=)
if num1 != num2:
    print("The numbers are not equal.")
else:
    print("The numbers are equal.")